"""
TextColor: ANSI Escape Sequences for Color Coding
"""

class TextColor:
    RESET = '\033[0m'
    GREY = '\033[90m'
    RED = '\033[91m'
    GREEN = '\033[92m'
    YELLOW = '\033[93m'
    BLUE = '\033[94m'
    PINK = '\033[95m'
    CYAN = '\033[96m'
    BOLD = '\033[97m'
